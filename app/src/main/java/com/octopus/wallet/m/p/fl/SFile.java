package com.octopus.wallet.m.p.fl;

import com.octopus.wallet.m.b.S;

import java.io.File;

/**
 * Created by yoctopus on 3/22/17.
 */

public class SFile implements S {
    private int id;
    private File file;

    public SFile(int id, File file) {
        this.id = id;
        this.file = file;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public File getFile() {
        return file;
    }

    public void setFile(File file) {
        this.file = file;
    }
}

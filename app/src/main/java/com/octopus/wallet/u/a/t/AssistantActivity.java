package com.octopus.wallet.u.a.t;

import android.os.Bundle;
import android.support.annotation.NonNull;
import android.support.design.widget.BottomNavigationView;
import android.view.MenuItem;
import android.widget.TextView;

import com.octopus.wallet.R;
import com.octopus.wallet.u.a.BActivity;

import butterknife.ButterKnife;
import butterknife.InjectView;

public class AssistantActivity extends BActivity {
    @InjectView(R.id.message)
    TextView message;
    @InjectView(R.id.navigation)
    BottomNavigationView navigation;

    private BottomNavigationView.OnNavigationItemSelectedListener selectedListener
            = new BottomNavigationView.OnNavigationItemSelectedListener() {

        @Override
        public boolean onNavigationItemSelected(@NonNull MenuItem item) {
            switch (item.getItemId()) {
                case R.id.navigation_home:
                    message.setText(R.string.title_home);
                    return true;
                case R.id.navigation_dashboard:
                    message.setText(R.string.title_dashboard);
                    return true;
                case R.id.navigation_notifications:
                    message.setText(R.string.title_notifications);
                    return true;
            }
            return false;
        }

    };

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_assistant);
        setHasBackButton(true);
        ButterKnife.inject(this);
        navigation.setOnNavigationItemSelectedListener(selectedListener);
    }

}

package me.yoctopus.cac.pref;

public class Pref<T> {
    private String name;
    private T data;

    public Pref(String key, T data) {
        this.name = key;
        this.data = data;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public T get() {
        return data;
    }

    public void set(T data) {
        this.data = data;
    }
}
